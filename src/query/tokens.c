/*
 * src/query/tokens.c - hand-written regex tokenizer.
 *
 * Lime handles the grammar (LALR(1)); this file emits tokens the
 * grammar consumes.  Regex requires mode-sensitive lexing (inside
 * [...] a '-' is a range operator, outside it's a literal; inside
 * {~m} the contents are a count) so a mode-free generated tokenizer
 * is not a great fit -- hence the hand roll.
 *
 * Phase 3: ASCII-only support. Multi-byte UTF-8 sequences are handled
 * in Phase 3.5. For Phase 3, each "codepoint" is a single byte.
 */

#include "postgres.h"

#include "pg_tre/regex_ast.h"

#include <ctype.h>

/* Token IDs from tre_grammar.h (generated by Lime). */
#define TOK_PIPE          1
#define TOK_CONCAT        2
#define TOK_STAR          3
#define TOK_PLUS          4
#define TOK_QUESTION      5
#define TOK_LBRACE        6
#define TOK_APPROX        7
#define TOK_INT           8
#define TOK_RBRACE        9
#define TOK_COMMA         10
#define TOK_LPAREN        11
#define TOK_RPAREN        12
#define TOK_LITERAL       13
#define TOK_DOT           14
#define TOK_ANCHOR_START  15
#define TOK_ANCHOR_END    16
#define TOK_LBRACKET      17
#define TOK_RBRACKET      18
#define TOK_CARET         19
#define TOK_DASH          20

typedef enum TokenizerMode
{
	MODE_NORMAL,
	MODE_BRACKET,      /* inside [...] */
	MODE_BRACE,        /* inside {m,n} or {~N} */
	MODE_BRACE_APPROX  /* inside {~N} after seeing ~ */
} TokenizerMode;

typedef struct TokenizerState
{
	const char *input;
	int         len;
	int         pos;
	TokenizerMode mode;
	int         bracket_depth;
	int         brace_depth;
} TokenizerState;

/*
 * Initialize tokenizer state.
 */
static void
init_tokenizer(TokenizerState *ts, const char *input, int len)
{
	ts->input = input;
	ts->len = len;
	ts->pos = 0;
	ts->mode = MODE_NORMAL;
	ts->bracket_depth = 0;
	ts->brace_depth = 0;
}

/*
 * Peek at the next character without consuming it.
 */
static int
peek_char(TokenizerState *ts)
{
	if (ts->pos >= ts->len)
		return -1;
	return (unsigned char) ts->input[ts->pos];
}

/*
 * Consume and return the next character.
 */
static int
next_char(TokenizerState *ts)
{
	if (ts->pos >= ts->len)
		return -1;
	return (unsigned char) ts->input[ts->pos++];
}

/*
 * Parse an escape sequence starting after the backslash.
 * Returns the escaped codepoint or -1 on error.
 */
static int
parse_escape(TokenizerState *ts, TreParseCtx *ctx)
{
	int c = next_char(ts);

	switch (c)
	{
		case 'n': return '\n';
		case 't': return '\t';
		case 'r': return '\r';
		case '\\': return '\\';
		case '.': return '.';
		case '[': return '[';
		case ']': return ']';
		case '(': return '(';
		case ')': return ')';
		case '{': return '{';
		case '}': return '}';
		case '|': return '|';
		case '*': return '*';
		case '+': return '+';
		case '?': return '?';
		case '^': return '^';
		case '$': return '$';
		case '-': return '-';

		/* ASCII character classes (Phase 3: simple version) */
		case 'd': /* \d => [0-9] but for Phase 3 we return a special literal */
		case 'w': /* \w => [a-zA-Z0-9_] */
		case 's': /* \s => [ \t\n\r] */
			ctx->syntax_error = true;
			snprintf(ctx->errmsg, sizeof(ctx->errmsg),
					 "\\%c escapes not yet implemented in Phase 3", (char) c);
			return -1;

		case -1:
			ctx->syntax_error = true;
			snprintf(ctx->errmsg, sizeof(ctx->errmsg),
					 "unexpected end of pattern after backslash");
			return -1;

		default:
			/* Unknown escape; treat as literal */
			return c;
	}
}

/*
 * Parse a decimal integer starting at current position.
 * Returns the integer value and advances ts->pos.
 */
static int
parse_integer(TokenizerState *ts, TreParseCtx *ctx)
{
	int val = 0;
	int c;
	bool got_digit = false;

	while ((c = peek_char(ts)) != -1 && isdigit(c))
	{
		next_char(ts);
		val = val * 10 + (c - '0');
		got_digit = true;
	}

	if (!got_digit)
	{
		ctx->syntax_error = true;
		snprintf(ctx->errmsg, sizeof(ctx->errmsg),
				 "expected digit in repetition count");
		return -1;
	}

	return val;
}

/*
 * Main tokenization function.
 * Returns the token kind (> 0) or 0 on EOF, or -1 on error.
 */
int
tre_tokenize_next(TreParseCtx *ctx, TreToken *out)
{
	TokenizerState *ts;
	int c;

	/* Initialize tokenizer state on first call */
	if (ctx->tokenizer_state == NULL)
	{
		ts = (TokenizerState *) palloc0(sizeof(TokenizerState));
		init_tokenizer(ts, ctx->input, ctx->input_len);
		ctx->tokenizer_state = ts;
	}
	else
	{
		ts = (TokenizerState *) ctx->tokenizer_state;
	}

	/* EOF */
	if (ts->pos >= ts->len)
		return 0;

	c = next_char(ts);

	/* Handle mode-specific tokens */
	if (ts->mode == MODE_BRACKET)
	{
		/* Inside [...] */
		if (c == ']')
		{
			ts->bracket_depth--;
			if (ts->bracket_depth == 0)
				ts->mode = MODE_NORMAL;
			return TOK_RBRACKET;
		}
		else if (c == '-')
		{
			return TOK_DASH;
		}
		else if (c == '^')
		{
			/* ^ is CARET only at the start of the bracket expression */
			/* We'll let the parser handle the context */
			return TOK_CARET;
		}
		else if (c == '\\')
		{
			int esc = parse_escape(ts, ctx);
			if (esc < 0)
				return -1;
			out->cp = esc;
			return TOK_LITERAL;
		}
		else
		{
			out->cp = c;
			return TOK_LITERAL;
		}
	}
	else if (ts->mode == MODE_BRACE || ts->mode == MODE_BRACE_APPROX)
	{
		/* Inside {m,n} or {~N} */
		if (c == '}')
		{
			ts->brace_depth--;
			if (ts->brace_depth == 0)
				ts->mode = MODE_NORMAL;
			return TOK_RBRACE;
		}
		else if (c == ',')
		{
			return TOK_COMMA;
		}
		else if (c == '~' && ts->mode == MODE_BRACE)
		{
			ts->mode = MODE_BRACE_APPROX;
			/* Parse the integer that follows ~ */
			int k = parse_integer(ts, ctx);
			if (k < 0)
				return -1;
			out->i = k;
			return TOK_APPROX;
		}
		else if (isdigit(c))
		{
			/* Put the digit back and parse the full integer */
			ts->pos--;
			int val = parse_integer(ts, ctx);
			if (val < 0)
				return -1;
			out->i = val;
			return TOK_INT;
		}
		else
		{
			ctx->syntax_error = true;
			snprintf(ctx->errmsg, sizeof(ctx->errmsg),
					 "unexpected character '%c' inside {}", (char) c);
			return -1;
		}
	}

	/* Normal mode */
	switch (c)
	{
		case '|':
			return TOK_PIPE;

		case '*':
			return TOK_STAR;

		case '+':
			return TOK_PLUS;

		case '?':
			return TOK_QUESTION;

		case '(':
			return TOK_LPAREN;

		case ')':
			return TOK_RPAREN;

		case '.':
			return TOK_DOT;

		case '^':
			return TOK_ANCHOR_START;

		case '$':
			return TOK_ANCHOR_END;

		case '[':
			ts->bracket_depth++;
			ts->mode = MODE_BRACKET;
			return TOK_LBRACKET;

		case '{':
			ts->brace_depth++;
			ts->mode = MODE_BRACE;
			return TOK_LBRACE;

		case '\\':
			{
				int esc = parse_escape(ts, ctx);
				if (esc < 0)
					return -1;
				out->cp = esc;
				return TOK_LITERAL;
			}

		default:
			/* Regular literal character */
			out->cp = c;
			return TOK_LITERAL;
	}
}
